const mongoose = require('mongoose');
const aboutSchema = new mongoose.Schema({
  WhoWeAre: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});